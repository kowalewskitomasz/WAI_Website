            <h1>Barki</h1>
            <h3>Wyciskanie żołnierskie</h3>
            <p>Aby ćwiczyć mięśnie barków wykonuję jedynie jedno ćwiczenie, a jest nim wyciskanie żołnierskie. Polega ono na wyciskaniu sztangi pionowo do góry w pozycji stojącej lub siedzącej. Stajemy w rozkroku nieco szerszym, niż barki, klatka piersiowa wypchnięta ku przodowi, zachowana naturalna krzywizna kręgosłupa. Podczas ćwiczenia pracują tylko ramiona i barki.</p>
